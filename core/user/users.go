// Email 生成随机的邮箱
// @return email 邮箱账号
func (u *User) Email() (email string) {
	suffix := []string{
		"@qq.com", "@163.com", "@126.com", "@189.com",
		"@outlook.com", "gmail.com", "yahoo.com",
	}
	_, name := u.EnglishName()
	email = fmt.Sprintf("%s%s", name, suffix[u.int.Int(0, len(suffix))])
	return
}
